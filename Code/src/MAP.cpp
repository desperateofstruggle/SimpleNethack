#include"MAP.h"

MAP::MAP() {
	char copymap[100][100] = {
		"                   -----     ----------------                          -----------",
		"--------          #.@M.|     |....M......M..|##########################.....M....|",
		"|KM....|        ###|...##### |..............|                          |D.A|..M..|",
		"|M...M.|        #  |...|  #  |..............|                          |.M.|....k|",
		"|......|      ###  |.M.|  ## |..A...........|                          -.---------",
		"--------      #    -----   # -------------.--     ------------          #",
		" #######    ###             ######        #########..........|        ###",
		"       #    #                    #                |........A.|       -.-.-",
		"       ## ###                    ###             #|....M.....|      #....|",
		"     ---.-#                        #             #...M........###### |.M.|",
		"     |.M.|#                        ###         ###|..........|       |...|",
		"     |...|#                          #         # #------------       |.M.|",
		"     |TMD|#                          ###     ###                     |..C|",
		"     -----                             #     #                       -----",
		"                                      -.-----#",
		"                                      |.S..M.#",
		"                                      |..M..|",
		"                                      -------",
	};
	for (int i = 0; i < 100; i++) {
		for (int j = 0; j < 100; j++) {
			map[i][j] = copymap[i][j];
		}
	}
	floor = 1;
}

MAP::~MAP() {}

void MAP::printmap() {
	switch (floor)
	{
	case 1:
		for (int i = 0; i < 18; i++) {
			for (int j = 0; j < 100; j++) {
			cout << map[i][j];
			}
		cout << '\n';
		}
		break;
	default:
		break;
	}
	cout << endl;
}

void MAP::changemap(int pos_x, int pos_y, char copy_) {
	map[pos_y][pos_x] = copy_;
}
char MAP::getmap(int pos_x, int pos_y) {
	return map[pos_y][pos_x];
}
